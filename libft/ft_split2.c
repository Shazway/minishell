#include "libft.h"

void	split_worker(const char *s, int *i, int *upto, char c)
{
	while (s[*i] != c && s[*i])
		*i = *i + 1;
	*upto = *i;
	while (s[*i] == c && s[*i])
		*i = *i + 1;
}
